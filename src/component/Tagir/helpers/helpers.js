export const returnNameTagir = (name) => {
  let reverseStrName = name.split("").reverse().join("");
  let splitPrice =
    reverseStrName.indexOf(" ") != -1
      ? /\s(.+)/.exec(reverseStrName)[1]
      : reverseStrName;
  let reverseBackStrName = splitPrice.split("").reverse().join("");

  return reverseBackStrName;
};

